        private byte[] GetSerializedObject(CustomerProduct cls)
        {
            byte[] clscustomerProductId = ObjectToByteArray(cls.CustomerProductId);
            byte[] clscustomerId = ObjectToByteArray(cls.CustomerId);
            byte[] clsproductId = ObjectToByteArray(cls.ProductId);

            byte[] clsArray = new byte[clscustomerProductId.Length + clscustomerId.Length + clsproductId.Length];
            Array.Copy(clscustomerProductId, 0, clsArray, 0, clscustomerProductId.Length);
            Array.Copy(clscustomerId, 0, clsArray, 0 + clscustomerProductId.Length, clscustomerId.Length);
            Array.Copy(clsproductId, 0, clsArray, 0 + clscustomerProductId.Length + clscustomerId.Length, clsproductId.Length);

            return clsArray;

        }

        /// <summary>
        /// Determines of the current concurrency array is equal to what's in the database.
        /// </summary>
        /// <returns>True, if the data in memory is equal to whats in the database, otherwise false.</returns>
        protected bool IsDataEqual()
        {
            BusinessLayer.CustomerProduct obj = new BusinessLayer.CustomerProduct(CustomerProductId);
            byte[] myArray = _concurrencyArray;
            byte[] clsArray = GetSerializedObject(obj);

            if (myArray.Length != clsArray.Length)
            {
                return false;
            }

            for (int i = 0; i < myArray.Length; i++)
            {
                if (myArray[i] != clsArray[i])
                {
                    return false;
                }
            }
            return true;
        }

        private byte[] ObjectToByteArray(object obj)
        {
            if (obj == null)
                return new byte[] {};

            BinaryFormatter bf = new BinaryFormatter();
            using (MemoryStream ms = new MemoryStream())
            {
                bf.Serialize(ms, obj);
                return ms.ToArray();
            }
        }

        /// <summary>
        /// Converts an instance of an object to a string format
        /// </summary>
        /// <param name="format">Specifies if it should convert to XML, BSON, or JSON</param>
        /// <returns>The object, converted to a string representation</returns>
        public string ToString(BusinessLayer.SerializationFormats format)
        {
            SerializableCustomerProduct serializableCustomerProduct = new SerializableCustomerProduct();
            serializableCustomerProduct.CustomerProductId = IsNull(BusinessLayer.CustomerProduct.Fields.CustomerProductId)
                ? (int?) null : _customerProductId;
            serializableCustomerProduct.CustomerId = IsNull(BusinessLayer.CustomerProduct.Fields.CustomerId)
                ? (int?) null : _customerId;
            serializableCustomerProduct.ProductId = IsNull(BusinessLayer.CustomerProduct.Fields.ProductId)
                ? (int?) null : _productId;
            serializableCustomerProduct.SerializationIsUpdate = _layerGenIsUpdate;

            if (format == BusinessLayer.SerializationFormats.Json)
            {
                return Newtonsoft.Json.JsonConvert.SerializeObject(serializableCustomerProduct);
            }

            if (format == BusinessLayer.SerializationFormats.Xml)
            {
                System.Xml.Serialization.XmlSerializer xType = new System.Xml.Serialization.XmlSerializer(serializableCustomerProduct.GetType());

                using (StringWriter sw = new StringWriter())
                {
                    xType.Serialize(sw, serializableCustomerProduct);

                    return sw.ToString();
                }
            }

            if (format == BusinessLayer.SerializationFormats.BsonBase64)
            {
                using (System.IO.MemoryStream ms = new System.IO.MemoryStream())
                {
                    using (Newtonsoft.Json.Bson.BsonWriter writer = new Newtonsoft.Json.Bson.BsonWriter(ms))
                    {
                        Newtonsoft.Json.JsonSerializer serializer = new Newtonsoft.Json.JsonSerializer();
                        serializer.Serialize(writer, serializableCustomerProduct);
                    }
                    return Convert.ToBase64String(ms.ToArray());
                }
            }

            return "";
        }


